my_list = [1, 2, 3, 5, 4]
print(my_list)
my_list.append(6)  # Adding an element to the end
print(my_list)
my_list.insert(0, 0)  # Inserting an element at the beginning
print(my_list)
my_list.remove(3)  # Removing an element by value
print(my_list)
my_list.pop()  # Removing the last element
print(my_list)
my_list.pop(0)  # Removing the first element
print(my_list)
print(my_list.index(2))  # Finding the index of an element
my_list.sort()  # Sorting the list


people = ["Alice", "Bob", "Charlie"]
print(people)
print(people[0])  # Accessing the first element
print(people[1])  # Accessing the second element
print(people[2])  # Accessing the third element
print(people[-1])  # Accessing the last element
print(people[-2])  # Accessing the second to last element
print(len(people))  # Getting the length of the list
print(people[0:2])  # Slicing the first two elements
print(people[1:])  # Slicing from the second element to the end
print(people[:2])  # Slicing the first two elements
print(people[1:3])  # Slicing from the second to the third element
print(people[::2])  # Slicing every second element
print(people[::-1])  # Reversing the list
print(people[1:3])  # Slicing from the second to the third element
print(people[1:])  # Slicing from the second element to the end
print(people[:2])  # Slicing the first two elements
